          <h3>Collection Development Policy and Procedures</h3>
          <h4>Introduction</h4>
          <p>
            The Heritage Archives serves as the final repository for the
            historical records of Rocky Mountain College and its predecessor
            schools. Its primary purpose is to document the history of the
            College and to provide source material for administrators, faculty,
            students, alumni, and other members of the College community, as
            well as scholars, authors, and other interested persons who seek to
            evaluate the impact of the College's activities on Billings,
            Yellowstone County, and Montana.
          </p>
          <h4>Core Mission</h4>
          <p>The core mission of the Heritage Archives is as follows:</p>
          <ul>
            <li>
              To collect, preserve, describe, organize, and make available
              records of historical, legal, fiscal, and/or administrative value
              to Rocky Mountain College*.
            </li>
            <li>
              To provide adequate facilities for the retention and preservation
              of such records.
            </li>
            <li>
              To provide information services that will assist the operation of
              the College.
            </li>
            <li>
              To serve research and scholarship by making available and
              encouraging the use of its collections by members of the College
              and the community at large.
            </li>
            <li>
              To promote knowledge and understanding of the origins, aims,
              programs, and goals of the College and of the development of these
              aims, goals, and programs.
            </li>
            <li>
              To facilitate efficient records management within the College
              offices.
            </li>
          </ul>
          <p>
            *Rocky Mountain College refers to Rocky Mountain College and its
            predecessor schools, including College of Montana, Montana Wesleyan
            College, Intermountain Union College, and Polytechnic Institute.
          </p>
          <h3>Collection Development</h3>
          <p>
            The records of Rocky Mountain College* emanate from many sources.
            The Archives must rely on the cooperation and support of the
            administration, deans, directors, faculty, students, and alumni to
            ensure that material of historical value is collected and preserved.
            The Heritage Archives may collect material in the following
            categories from administrative and academic units of the College.
          </p>
          <p>
            I. OFFICIAL RECORDS, PAPERS, AND PUBLICATIONS OF ROCKY MOUNTAIN
            COLLEGE
          </p>
          <p>
            The official records encompass the records or papers generated by or
            received from various administrative offices of Rocky Mountain
            College in the conduct of their business. These records may include:
          </p>
          <p>
            A. Minutes, memoranda, correspondence, and reports of the Board of
            Trustees.
          </p>
          <p>
            B. Records of the Office of the President, including correspondence,
            administrative subject files, and reports.
          </p>
          <p>
            C. Correspondence, subject files, and reports of the Office of
            Academic Affairs.
          </p>
          <p>
            D. Minutes, memoranda, and reports of all major academic and
            administrative commissions, councils, and committees including the
            Executive Council, Administrative Council, and Faculty Executive
            Council.
          </p>
          <p>
            E. Departmental records, including reports, correspondence, syllabi,
            and records of the continuing education office.
          </p>
          <p>F. Accreditation reports and supporting documentation.</p>
          <p>G. Annual budget and audit reports.</p>
          <p>H. Personnel records or retired, resigned, or deceased faculty.</p>
          <p>
            I. Records of the registrar, including timetables and class
            schedules, enrollment reports, graduation rosters, and other reports
            issued on a regular basis.
          </p>
          <p>J. Minutes and records of the Alumni Association.</p>
          <p>
            K. Reports and agendas of the Office of Student Affairs, including
            admissions, financial aid, and student services.
          </p>
          <p>
            L. Reports and records of the development office, including annual
            fund, capital fund, fund development, constituent relations, and
            public relations and records of student organizations.
          </p>
          <p>
            M. All publications, newsletters, or booklets distributed in the
            name of Rocky Mountain College including: catalogs, College
            directories, faculty/staff rosters, faculty and administration
            newsletters and publications, alumni publications, and ephemeral
            material.
          </p>
          <p>
            N. Audiovisual materials documenting the development of the College,
            such as photographs, negatives, slides, motion picture films, oral
            history interviews, and audio and video tapes, discs, and
            recordings.
          </p>
          <p>
            O. Maps, prints, and drawings documenting the physical growth of the
            College.
          </p>
          <p>P. Reports of research projects, including grant records.</p>
          <p>Q. Artifacts relating to the history of Rocky Mountain College.</p>
          <p>
            The official administrative records of Rocky Mountain College
            (correspondence, reports, and subject files) should be inactive and
            no longer of use in the activities of the originating office. They
            should be forwarded to the archives according to a schedule agreed
            upon by the archives staff and the office of origin. A
            representative of the archives will consult with the various offices
            to arrange for orderly transfer of non-current material destined for
            the archives. When material is forwarded it should be accompanied by
            an inventory describing the records being transferred. The
            originating office may wish to place restrictions on access to
            non-current records.
          </p>
          <p>
            II. PERSONAL AND PROFESSIONAL PAPERS OF ROCKY MOUNTAIN COLLEGE
            FACULTY
          </p>
          <p>
            As an important part of its mission of documenting the internal life
            of the Rocky Mountain College community, the Heritage Archives
            actively seeks to acquire, organize, and make available the personal
            and professional papers of the Rocky Mountain College faculty.
          </p>
          <p>
            Faculty papers offer insight into the history and operation of the
            College that otherwise may be lost by relying only on official
            administrative records. They reveal professional interests and
            opinions that frequently clarify matters mentioned in the official
            records of the central administration. Faculty papers document the
            academic life of the College and relate one's academic career to his
            or her total interests, thereby constituting an important historical
            record. Personal viewpoints expressed in private correspondence and
            documentation resulting from service on academic committees provide
            a better basis for understanding the offices. Without a broad range
            of faculty papers the formal official accounts of the College are
            often misleading.
          </p>
          <p>
            The following reflects the types of documentation sought by the
            Heritage Archives:
          </p>
