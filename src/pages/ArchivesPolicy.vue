            correspondence and memoranda generated in the course of conducting
            College business.
          </p>
          <p>
            b. Professional: outgoing and incoming correspondence relating to
            all facets of one's academic career, colleagues, publishers,
            professional societies, students, etc.
          </p>
          <p>
            c. Personal: letters to/from friends, relatives, acquaintances,
            business correspondence, etc.
          </p>
          <p>
            2. Biographical material: resumes, vita sheets, bibliographical
            sketches, chronologies, genealogies, newspaper clippings, personal
            memoirs, etc.
          </p>
          <p>3. Photographs: prints, slides, negatives</p>
          <p>4. Tape recordings: lectures, speeches, discussions, etc.</p>
          <p>5. Class lecture notes and syllabi.</p>
          <p>6. Research files.</p>
          <p>7. Departmental or committee minutes and records.</p>
